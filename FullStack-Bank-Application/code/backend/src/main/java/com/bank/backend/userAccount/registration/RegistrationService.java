    private final UserAccountService userAccountService;
    private final ConfirmationTokenService confirmationTokenService;
    private final EmailValidator emailValidator;
    private final EmailSender emailSender;

    /**
     * Register new user and send email for confirmation
     * @param request registration request object
     * @return conformation token
     */
    public String register(RegistrationRequest request) {
         if (!emailValidator.test(request.getEmail()))
             throw new IllegalStateException("Invalid Email");

        String token = userAccountService.signupNewUser(
                new UserAccount(
                        request.getFirstName(),
                        request.getLastName(),
                        request.getEmail(),
                        request.getUsername(),
                        request.getPassword(),
                        request.getDateOfBirth(),
                        UserAccountRole.USER
                )
        );
        emailSender.send(
                request.getEmail(),
                buildEmail(
                        request.getFirstName(),
                        "http://localhost:8080/api/user/registration/confirm?token=" + token
                )
        );
        return token;
    }

    /**
     * Cofirm token in database and enable user
     * @param token token string
     * @return confirmed
     */
    @Transactional
    public String confirmToken(String token) {
        ConfirmationToken conformationToken = confirmationTokenService
                .getToken(token).orElseThrow(()->new IllegalStateException("Token not Found"));

        if(conformationToken.getConfirmedAt() != null)
            throw new IllegalStateException("Email already Confirmed");

        if(conformationToken.getExpiresAt().isBefore(LocalDateTime.now()))
            throw new IllegalStateException("Email already Confirmed");

        confirmationTokenService.setConfirmedAt(token);

        userAccountService.enableUserAccount(conformationToken.getUser().getEmail());

        return "confirmed";
    }

